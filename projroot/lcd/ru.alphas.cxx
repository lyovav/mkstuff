static uint8_t cp1251_to_HD44780(uint8_t sym);

char const* cp1251_to_lcd(char const* source)
{
    size_t l = strlen(source);

    uint8_t* p = (uint8_t*)source;
    for (size_t i=0; i < l; i++)
        p[i] = cp1251_to_HD44780(p[i]);

    return source;
}

